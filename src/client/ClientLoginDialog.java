    private void jbLoginActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jbLoginActionPerformed
    {//GEN-HEADEREND:event_jbLoginActionPerformed
        // TODO add your handling code here:
        String[] as = jtxtName.getText().trim().split("\\s+");
 
        if (as[0].length() > 0)
            m_pParent.tf.setText(as[0] + ':' + jtxtPassword.getText().trim().split("\\s+")[0]);
        else
            m_pParent.tf.setText("Anonymous");
        
        m_pParent.actionPerformed(new ActionEvent(m_pParent.login, ActionEvent.ACTION_FIRST-1, null));
        this.setVisible(false);
    }//GEN-LAST:event_jbLoginActionPerformed

